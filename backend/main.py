@app.post("/generate-note")
async def generate_obsidian_note(result: ValidationResult) -> dict:
    """
    Generate Obsidian markdown note from validation result.
    Writes to: {OBSIDIAN_VAULT}/assessments/{date}/{video_id}.md
    """
    try:
        # Create directory structure
        date_str = datetime.utcnow().strftime("%Y-%m-%d")
        assessments_dir = Path(OBSIDIAN_VAULT_PATH) / "assessments" / date_str
        assessments_dir.mkdir(parents=True, exist_ok=True)

        # Generate markdown content
        note_content = f"""---
video_id: {result.video_id}
title: {result.title}
routing: {result.routing}
confidence: {result.confidence}
assessed_at: {datetime.utcnow().isoformat()}
---

# {result.title}

## Assessment Results

| Dimension | Score |
|-----------|-------|
| Credibility | {result.credibility}/100 |
| Quality | {result.quality}/100 |
| Relevance | {result.relevance}/100 |
| Alignment | {result.alignment}/100 |

**Overall Confidence:** {result.confidence:.1%}

**Recommendation:** `{result.routing}`

## Assessment Metadata

- **Video ID:** `{result.video_id}`
- **Assessed:** {datetime.utcnow().isoformat()}
- **Source:** Dual-agent validation (Screening Agent + Critical Agent)

## Routing Logic

- **PROMOTE (>80):** High-quality, highly relevant content. Integrate immediately.
- **INBOX (60-80):** Moderate quality. Review for integration.
- **ARCHIVE (<60):** Low relevance or quality. Archive for reference.

---

*Generated by PCA Validation Layer*
"""

        # Write to file
        note_path = assessments_dir / f"{result.video_id}.md"
        with open(note_path, "w") as f:
            f.write(note_content)

        logger.info(f"Note generated: {note_path}")

        return {
            "success": True,
            "note_path": str(note_path),
            "video_id": result.video_id,
            "message": f"Note written to {note_path}"
        }

    except Exception as e:
        logger.error(f"Note generation failed: {e}")
        raise HTTPException(status_code=500, detail=str(e))


