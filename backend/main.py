#!/usr/bin/env python3
"""
FastAPI backend for PCA Sprint 5 Validation Layer
Handles deduplication checks and Obsidian note generation
"""

from fastapi import FastAPI, HTTPException, BackgroundTasks
from pydantic import BaseModel
from datetime import datetime
import os
import json
import logging
from pathlib import Path

# Configure logging
logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

app = FastAPI(
    title="PCA Validation Backend",
    description="Helper APIs for n8n validation workflow",
    version="1.0.0"
)

# Configuration from environment
NEO4J_HOST = os.getenv("NEO4J_HOST", "neo4j")
NEO4J_PORT = int(os.getenv("NEO4J_PORT", "7687"))
NEO4J_USER = os.getenv("NEO4J_USER", "neo4j")
NEO4J_PASSWORD = os.getenv("NEO4J_PASSWORD", "password")
OBSIDIAN_VAULT_PATH = os.getenv("OBSIDIAN_VAULT_PATH", "/vault")


# Models
class DeduplicationCheck(BaseModel):
    video_id: str


class ValidationResult(BaseModel):
    video_id: str
    title: str
    routing: str  # PROMOTE, INBOX, or ARCHIVE
    confidence: float
    credibility: int
    quality: int
    relevance: int
    alignment: int


class ObsidianNote(BaseModel):
    video_id: str
    title: str
    routing: str
    scores: dict
    assessment_date: str


# Endpoints

@app.get("/health")
async def health_check():
    """Health check endpoint"""
    return {
        "status": "healthy",
        "timestamp": datetime.utcnow().isoformat(),
        "neo4j": f"{NEO4J_HOST}:{NEO4J_PORT}",
        "obsidian_vault": OBSIDIAN_VAULT_PATH
    }


